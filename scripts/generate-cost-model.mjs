#!/usr/bin/env node
// REQ-PACK-001. Derives COMMAND_COST_MODEL from the REAL src/commands/*.ts source
// (the SAME gatePaidCall( grep ground truth blockrun-cli-agent-dx's REQ-DX-001/003
// established) and writes it as a plain object literal to
// src/core/cost-model.generated.ts — zero runtime filesystem access in the generated
// file itself, so the built dist/index.js never needs src/ to exist at runtime
// (REQ-PACK-002/003 — this is what fixes the published-package startup crash).
//
// Run via package.json's "prebuild" script (auto-invoked by `npm run build`), or
// directly after editing any src/commands/<name>.ts file's gatePaidCall( usage.
//
// The output IS committed to git (REQ-PACK-004) — `npm test` runs directly against
// TypeScript source with no prior build step, so a gitignored generated file would be
// absent on a fresh clone. test/unit/cost-model-generated-parity.test.ts mechanically
// re-derives this same mapping and fails if this file is ever stale.

import { readFileSync, readdirSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import path from "node:path";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = path.resolve(__dirname, "..");
const COMMANDS_DIR = path.join(REPO_ROOT, "src", "commands");
const OUT_PATH = path.join(REPO_ROOT, "src", "core", "cost-model.generated.ts");

function deriveCostModel() {
  const registry = {};
  for (const file of readdirSync(COMMANDS_DIR)) {
    if (!file.endsWith(".ts") || file === "shared.ts") continue;
    const name = file.replace(/\.ts$/, "");
    const src = readFileSync(path.join(COMMANDS_DIR, file), "utf8");
    registry[name] = src.includes("gatePaidCall(") ? "paid" : "free";
  }
  return registry;
}

function main() {
  const registry = deriveCostModel();
  const entries = Object.entries(registry)
    .sort(([a], [b]) => a.localeCompare(b))
    .map(([name, model]) => `  ${JSON.stringify(name)}: ${JSON.stringify(model)},`)
    .join("\n");

  const contents = `// AUTO-GENERATED by scripts/generate-cost-model.mjs — do not hand-edit.
// Regenerated automatically by `+ "`npm run build`" + `'s "prebuild" script, derived
// from the REAL src/commands/*.ts source (grep for \`gatePaidCall(\`) — REQ-PACK-001.
// Committed to git (REQ-PACK-004): npm test runs directly against source with no
// prior build step, so a gitignored file would be absent on a fresh clone.
// test/unit/cost-model-generated-parity.test.ts fails if this ever drifts from the
// real source.
export type CostModel = "free" | "paid";

export const COMMAND_COST_MODEL: Record<string, CostModel> = {
${entries}
};
`;

  writeFileSync(OUT_PATH, contents, "utf8");
  console.log(`generate-cost-model: wrote ${Object.keys(registry).length} entries to ${path.relative(REPO_ROOT, OUT_PATH)}`);
}

main();
